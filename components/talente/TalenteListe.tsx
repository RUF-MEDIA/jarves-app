// Interfaces
interface DateRange {
  from: Date | null;
  to: Date | null;
}

interface Betreuer {
  id: string;
  name: string;
}

interface Bewerber {
  id: string;
  autogeneratedNr: number;
  vorname: string | null;
  nachname: string;
  email: string | null;
  status: string | null;
  kategorie: number | null;
  jobfield: string[];
  gehaltswunschBruttoJahr: number | null;
  erstelltAm: string;
  letzteAenderungAm: string | null;
  betreuer: Betreuer | null;
  // Optional dynamic fields can be added here
  [key: string]: string | number | boolean | Betreuer | DateRange | null | string[];
}

