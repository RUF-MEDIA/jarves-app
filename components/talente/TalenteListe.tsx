// Star Rating Component
const StarRating: React.FC<{ rating: number | null }> = ({ rating }) => {
  return (
    <div className="flex">
      {[1, 2, 3, 4, 5].map((star) => (
        <Star key={star} className={`h-4 w-4 ${rating !== null && star <= rating ? 'text-yellow-400 fill-current' : 'text-gray-300'}`} />
      ))}
    </div>
  );
};

// Status Select Filter Component
const StatusSelectFilter: React.FC<{ column: Column<Bewerber, unknown>; options: string[] }> = ({ column, options }) => {
  const [selectedValue, setSelectedValue] = useState<string>('');

  useEffect(() => {
    column.setFilterValue(selectedValue || undefined);
  }, [selectedValue, column]);

  const handleReset = () => {
    setSelectedValue('');
    column.setFilterValue(undefined);
  };

  return (
    <div className="space-y-2">
      <Select value={selectedValue} onValueChange={(value) => setSelectedValue(value)}>
        <SelectTrigger>
          <SelectValue placeholder="Filter auswählen" />
        </SelectTrigger>
        <SelectContent>
          {options.map((option) => (
            <SelectItem key={option} value={option}>
              {option}
            </SelectItem>
          ))}
        </SelectContent>
      </Select>
      {selectedValue && (
        <Button variant="ghost" size="sm" onClick={handleReset}>
          Filter zurücksetzen
        </Button>
      )}
    </div>
  );
};

// Star Rating Filter Component
const StarRatingFilter: React.FC<{ column: Column<Bewerber, unknown> }> = ({ column }) => {
  const [rating, setRating] = useState<number | null>(null);

  useEffect(() => {
    column.setFilterValue(rating);
  }, [rating, column]);

  const handleReset = () => setRating(null);

  return (
    <div className="flex flex-col space-y-2 p-2">
      <Label>Mindestbewertung</Label>
      <div className="flex">
        {[1, 2, 3, 4, 5].map((star) => (
          <Star
            key={star}
            className={`h-6 w-6 cursor-pointer ${rating !== null && star <= rating ? 'text-yellow-400 fill-current' : 'text-gray-300'}`}
            onClick={() => setRating(star === rating ? null : star)}
            aria-label={`Bewertung ${star}`}
          />
        ))}
      </div>
      {rating !== null && (
        <Button variant="ghost" size="sm" onClick={handleReset}>
          Filter zurücksetzen
        </Button>
      )}
    </div>
  );
};

// Render Cell Function
const renderCell = (key: string, row: Row<Bewerber>) => {
  const value = row.getValue(key);
  switch (key) {
    case 'vorname':
    case 'nachname':
      return (
        <Link href={`/talente/${row.original.id}`} className="text-blue-600 hover:underline" target="_blank" rel="noopener noreferrer">
          {value as string}
        </Link>
      );
    case 'kategorie':
      return <StarRating rating={value as number | null} />;
    case 'betreuer':
      return <div className="w-[200px]">{(value as Bewerber['betreuer'])?.name || '-'}</div>;
    case 'erstelltAm':
    case 'letzteAenderungAm':
      return <div className="w-[100px]">{value ? format(new Date(value as string), 'dd.MM.yyyy') : '-'}</div>;
    case 'gehaltswunschBruttoJahr':
      return <div className="w-[150px]">{value ? `${Number(value).toLocaleString('de-DE')} €` : '-'}</div>;
    case 'jobfield':
      return <div className="w-[200px]">{(value as string[]).join(', ')}</div>;
    case 'email':
      return value ? (
        <Link href={`mailto:${value}`} className="text-blue-600 hover:underline">
          {value as string}
        </Link>
      ) : (
        '-'
      );
    default:
      return <div className="w-[200px]">{value?.toString() || '-'}</div>;
  }
};

// Optimized createColumns function
const createColumns = (data: Bewerber[]): ColumnDef<Bewerber>[] => {
  const allFields = new Set<string>();
  data.forEach((item) => Object.keys(item).forEach((key) => allFields.add(key)));

  allFields.delete('id');

  return Array.from(allFields).map(
    (key): ColumnDef<Bewerber> => ({
      accessorKey: key,
      header: ({ column }) => {
        const isNameColumn = key === 'vorname' || key === 'nachname';

        return (
          <div className="flex items-center space-x-2">
            <DropdownMenu>
              <DropdownMenuTrigger asChild>
                <Button
                  variant="ghost"
                  size="sm"
                  className={`-ml-3 h-8 data-[state=open]:bg-accent ${isNameColumn ? 'ml-0' : ''}`}
                  aria-label={`${columnLabels[key] || key} Optionen`}
                >
                  <span>{columnLabels[key] || key.charAt(0).toUpperCase() + key.slice(1)}</span>
                  {column.getCanSort() && <ArrowUpDown className="ml-2 h-4 w-4" />}
                </Button>
              </DropdownMenuTrigger>
              <DropdownMenuContent align="start" className="w-80 p-4">
                <div className="text-base font-semibold mb-2">Optionen für {columnLabels[key] || key}</div>
                <div className="border-b my-2"></div>
                {column.getCanSort() && (
                  <div className="mb-4">
                    <div className="font-medium">Sortieren</div>
                    <DropdownMenuItem onClick={() => column.toggleSorting(false)}>
                      <ArrowUpDown className="mr-2 h-3.5 w-3.5 text-muted-foreground/70" />
                      Aufsteigend
                    </DropdownMenuItem>
                    <DropdownMenuItem onClick={() => column.toggleSorting(true)}>
                      <ArrowUpDown className="mr-2 h-3.5 w-3.5 text-muted-foreground/70" />
                      Absteigend
                    </DropdownMenuItem>
                  </div>
                )}
                <div className="border-b my-2"></div>
                <div>
                  <div className="font-medium mb-2">Filter</div>
                  {key === 'erstelltAm' || key === 'letzteAenderungAm' ? (
                    <DateRangeFilter column={column} />
                  ) : key === 'autogeneratedNr' || key === 'gehaltswunschBruttoJahr' ? (
                    <NumberRangeFilter column={column} />
                  ) : key === 'status' ? (
                    <StatusSelectFilter column={column} options={['aktiv', 'inaktiv', 'vermittelt', 'on hold', 'nicht kontaktieren']} />
                  ) : key === 'kategorie' ? (
                    <StarRatingFilter column={column} />
                  ) : isNameColumn ? (
                    <div className="text-sm text-gray-600">Kein Filter verfügbar</div>
                  ) : (
                    <Input
                      placeholder="Suchen..."
                      value={(column.getFilterValue() as string) ?? ''}
                      onChange={(event) => column.setFilterValue(event.target.value)}
                      className="max-w-sm"
                    />
                  )}
                </div>
              </DropdownMenuContent>
            </DropdownMenu>
          </div>
        );
      },
      cell: ({ row }) => renderCell(key, row),
      sortingFn: (rowA, rowB, columnId) => {
        const a = rowA.getValue(columnId);
        const b = rowB.getValue(columnId);

        if (a === null || a === undefined) return 1;
        if (b === null || b === undefined) return -1;
        if (a === b) return 0;

        if (typeof a === 'number' && typeof b === 'number') {
          return a - b;
        }
        if (typeof a === 'string' && typeof b === 'string') {
          return a.localeCompare(b);
        }
        if (key === 'betreuer') {
          return ((a as Bewerber['betreuer'])?.name || '').localeCompare((b as Bewerber['betreuer'])?.name || '');
        }
        if (key === 'erstelltAm' || key === 'letzteAenderungAm') {
          return new Date(a as string).getTime() - new Date(b as string).getTime();
        }
        if (key === 'jobfield') {
          return (a as string[]).join(',').localeCompare((b as string[]).join(','));
        }
        return 0;
      },
      filterFn: (row, id, filterValue) => {
        if (key === 'autogeneratedNr' || key === 'gehaltswunschBruttoJahr') {
          const value = row.getValue(id) as number;
          const [min, max] = filterValue as [number, number];
          return value >= min && value <= max;
        }
        if (key === 'kategorie') {
          const rating = row.getValue(id) as number | null;
          if (filterValue === null) return true;
          if (filterValue === 0) return rating === null || rating === 0;
          return rating !== null && rating >= filterValue;
        }
        if (key === 'status') {
          const status = row.getValue(id) as string;
          return filterValue === undefined || status === filterValue;
        }
        if (key === 'erstelltAm' || key === 'letzteAenderungAm') {
          const date = row.getValue(id) as string;
          const cellDate = new Date(date);
          const { from, to } = filterValue as DateRange;

          if (from && to) {
            return cellDate >= from && cellDate <= to;
          }
          if (from) {
            return cellDate >= from;
          }
          if (to) {
            return cellDate <= to;
          }
          return true;
        }
        if (key === 'jobfield') {
          const jobfields = row.getValue(id) as string[];
          return jobfields.some((job) => job.toLowerCase().includes((filterValue as string).toLowerCase()));
        }
        const cellValue = row.getValue(id);
        if (typeof cellValue === 'string') {
          return cellValue.toLowerCase().includes((filterValue as string).toLowerCase());
        }
        return String(cellValue)
          .toLowerCase()
          .includes((filterValue as string).toLowerCase());
      },
    })
  );
};

// TalentList Component
export function TalentListe() {
  const [sorting, setSorting] = useState<SortingState>([]);
  const [columnFilters, setColumnFilters] = useState<ColumnFiltersState>([]);
  const [columnVisibility, setColumnVisibility] = useState<VisibilityState>({});
  const [rowSelection, setRowSelection] = useState({});
  const [data, setData] = useState<Bewerber[]>([]);
  const [filterLetter, setFilterLetter] = useState('ALL');
  const [isBulkActionSidebarOpen, setIsBulkActionSidebarOpen] = useState(false);
  const [newStatus, setNewStatus] = useState('');
  const [newBetreuer, setNewBetreuer] = useState('');
  const [newKategorie, setNewKategorie] = useState('');
  const [newVerknuepfung, setNewVerknuepfung] = useState('');
  const [betreuerList, setBetreuerList] = useState<{ id: string; name: string }[]>([]);
  const [isLoading, setIsLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  // Initial data loading
  useEffect(() => {
    const fetchTalente = async () => {
      setIsLoading(true);
      setError(null);
      try {
        const response = await fetch('/api/getTalenteTest');
        if (!response.ok) {
          throw new Error(`HTTP error! status: ${response.status}`);
        }
        const talente = await response.json();
        setData(talente);
      } catch (error) {
        console.error('Error loading Talente:', error);
        setError('Error loading Talente. Please try again later.');
      } finally {
        setIsLoading(false);
      }
    };
    fetchTalente();
  }, []);

  // Load Betreuer list
  useEffect(() => {
    const fetchBetreuerList = async () => {
      try {
        const response = await fetch('/api/getBetreuerList');
        if (response.ok) {
          const data = await response.json();
          setBetreuerList(data);
        }
      } catch (error) {
        console.error('Error loading Betreuer:', error);
      }
    };

    fetchBetreuerList();
  }, []);

  // Column definition including the selection column
  const columns = useMemo(() => {
    const allColumns = createColumns(data);
    const selectColumn: ColumnDef<Bewerber> = {
      id: 'select',
      header: ({ table }) => (
        <Checkbox
          checked={table.getIsAllPageRowsSelected()}
          onCheckedChange={(value) => table.toggleAllPageRowsSelected(!!value)}
          aria-label="Select all"
        />
      ),
      cell: ({ row }) => (
        <Checkbox checked={row.getIsSelected()} onCheckedChange={(value) => row.toggleSelected(!!value)} aria-label={`Select row ${row.id}`} />
      ),
      enableSorting: false,
      enableHiding: false,
    };
    return [selectColumn, ...allColumns];
  }, [data]);

  // Load column visibility from localStorage
  useEffect(() => {
    const savedColumnVisibility = localStorage.getItem('columnVisibility_talent-list');
    if (savedColumnVisibility) {
      const parsedVisibility = JSON.parse(savedColumnVisibility);
      setColumnVisibility(parsedVisibility);
    } else {
      const initialVisibility: VisibilityState = {};
      columns.forEach((column) => {
        if ('accessorKey' in column && typeof column.accessorKey === 'string') {
          initialVisibility[column.accessorKey] = standardColumns.includes(column.accessorKey as keyof Bewerber);
        }
      });
      setColumnVisibility(initialVisibility);
      localStorage.setItem('columnVisibility_talent-list', JSON.stringify(initialVisibility));
    }
  }, [columns]);

  // Determine available letters for filter
  const availableLetters = useMemo(() => {
    const letters = new Set(data.map((bewerber) => bewerber.nachname[0].toUpperCase()).filter(Boolean));
    return Array.from(letters).sort();
  }, [data]);

  // Apply letter filter
  const filteredData = useMemo(() => {
    if (filterLetter === 'ALL') return data;
    return data.filter((bewerber) => bewerber.nachname.toUpperCase().startsWith(filterLetter));
  }, [data, filterLetter]);

  // Table setup
  const table = useReactTable({
    data: filteredData,
    columns,
    state: {
      sorting,
      columnFilters,
      columnVisibility,
      rowSelection,
    },
    enableRowSelection: true,
    onRowSelectionChange: setRowSelection,
    onSortingChange: setSorting,
    onColumnFiltersChange: setColumnFilters,
    onColumnVisibilityChange: (updatedVisibility) => {
      setColumnVisibility(updatedVisibility);
      localStorage.setItem('columnVisibility_talent-list', JSON.stringify(updatedVisibility));
    },
    getCoreRowModel: getCoreRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    getRowId: (row) => row.id,
  });

  // Letter filter handler
  const handleFilterChange = useCallback(
    (letter: string) => {
      setFilterLetter(letter);
      table.resetPageIndex();
    },
    [table]
  );

  // Bulk update handler
  const handleBulkUpdate = async () => {
    const selectedIds = Object.keys(rowSelection);
    if (selectedIds.length === 0) return;

    const updates: Partial<Pick<Bewerber, 'status' | 'kategorie' | 'betreuerId'>> = {
      status: newStatus || undefined,
      kategorie: newKategorie ? Number(newKategorie) : undefined,
      betreuerId: newBetreuer || undefined,
    };

    try {
      const response = await fetch('/api/bulkUpdateTalent', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ ids: selectedIds, updates }),
      });

      if (response.ok) {
        toast.success('Bulk update successfully completed');
        setData((prevData) => prevData.map((bewerber) => (selectedIds.includes(bewerber.id) ? { ...bewerber, ...updates } : bewerber)));
        setIsBulkActionSidebarOpen(false);
        setRowSelection({});
        setNewStatus('');
        setNewKategorie('');
        setNewBetreuer('');
      } else {
        toast.error('Error performing bulk update');
        console.error('Error performing bulk update');
      }
    } catch (error) {
      toast.error('Error performing bulk update');
      console.error('Error performing bulk update:', error);
    }
  };

  // Bulk delete handler
  const handleBulkDelete = async () => {
    const selectedIds = Object.keys(rowSelection);
    if (selectedIds.length === 0) return;

    const confirmDelete = window.confirm('Are you sure you want to delete the selected Talente?');
    if (!confirmDelete) return;

    try {
      const response = await fetch('/api/bulkDeleteTalent', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ ids: selectedIds }),
      });

      if (response.ok) {
        toast.success('Bulk delete successfully completed');
        setData((prevData) => prevData.filter((bewerber) => !selectedIds.includes(bewerber.id)));
        setIsBulkActionSidebarOpen(false);
        setRowSelection({});
      } else {
        toast.error('Error performing bulk delete');
        console.error('Error performing bulk delete');
      }
    } catch (error) {
      toast.error('Error performing bulk delete');
      console.error('Error performing bulk delete:', error);
    }
  };

  if (isLoading) {
    return <div>Loading Talente...</div>;
  }

  if (error) {
    return <div>Error: {error}</div>;
  }

  return (
    <div className="space-y-4">
      {/* Toast Container for notifications */}
      <ToastContainer />

      {/* Filter and search bar */}
      <div className="flex items-center justify-between">
        <div className="flex items-center space-x-2 overflow-x-auto">
          <LetterFilter availableLetters={availableLetters} currentFilter={filterLetter} onFilterChange={handleFilterChange} />
        </div>
        <div className="flex items-center space-x-2">
          <GlobalSearch table={table} />
          <ColumnSelector table={table} columnLabels={columnLabels} listId="talent-list" />
        </div>
      </div>

      {/* Table */}
      <div className="rounded-md border bg-white">
        <ScrollArea className="w-full overflow-auto" style={{ maxWidth: '100%' }}>
          <Table style={{ minWidth: '1000px' }}>
            <TableHeader>
              {table.getHeaderGroups().map((headerGroup) => (
                <TableRow key={headerGroup.id}>
                  {headerGroup.headers.map((header) => (
                    <TableHead key={header.id} className="bg-gray-100">
                      {header.isPlaceholder ? null : flexRender(header.column.columnDef.header, header.getContext())}
                    </TableHead>
                  ))}
                </TableRow>
              ))}
            </TableHeader>
            <TableBody>
              {table.getRowModel().rows?.length ? (
                table.getRowModel().rows.map((row) => (
                  <TableRow key={row.id} data-state={row.getIsSelected() ? 'selected' : undefined}>
                    {row.getVisibleCells().map((cell) => (
                      <TableCell key={cell.id}>{flexRender(cell.column.columnDef.cell, cell.getContext())}</TableCell>
                    ))}
                  </TableRow>
                ))
              ) : (
                <TableRow>
                  <TableCell colSpan={columns.length} className="h-24 text-center">
                    No results.
                  </TableCell>
                </TableRow>
              )}
            </TableBody>
          </Table>
          <ScrollBar orientation="horizontal" />
        </ScrollArea>
      </div>

      {/* Pagination */}
      <DataTablePagination table={table} />

      {/* Bulk action button */}
      <div className="flex justify-end">
        <Button variant="outline" onClick={() => setIsBulkActionSidebarOpen(true)} disabled={Object.keys(rowSelection).length === 0}>
          Bulk Actions
        </Button>
      </div>

      {/* Bulk Action Sidebar */}
      <BulkActionSidebar
        isOpen={isBulkActionSidebarOpen}
        selectedCount={Object.keys(rowSelection).length}
        onClose={() => setIsBulkActionSidebarOpen(false)}
        newStatus={newStatus}
        setNewStatus={setNewStatus}
        newBetreuer={newBetreuer}
        setNewBetreuer={setNewBetreuer}
        newKategorie={newKategorie}
        setNewKategorie={setNewKategorie}
        newVerknuepfung={newVerknuepfung}
        setNewVerknuepfung={setNewVerknuepfung}
        onDelete={handleBulkDelete}
        onUpdate={handleBulkUpdate}
      />
    </div>
  );
}
